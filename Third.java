    public static double getDistance(double lat1 ,double lng1,double lat2,double lng2) {
        double a = lat1 - lat2;
        double b = lng1 - lng2;
        double s = Math.sqrt(a*a*90+b*b*111);
        return s;
    }
    public static double min(double [] arr){
        double min = arr[0];
        for (int i = 1; i < arr.length; i++) {
            if (arr[i] < min) {
                min = arr[i];
            }
        }
        return min;
    }
